$$;

create index if not exists import_logs_created_at_idx on public.import_logs (created_at desc);
create index if not exists import_logs_numero_nf_idx on public.import_logs (numero_nf);

-- 5) RLS + policies basicas
alter table public.nfs enable row level security;
alter table public.nf_itens enable row level security;
